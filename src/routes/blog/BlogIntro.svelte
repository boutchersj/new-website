    <p>
        So, I thought it would be clever to make an acronym out of this challenge.
    </p>
    <br />
    <p>
        I've been struggling with sugar most of my life. Actually, that's a lie. I 
        didn't <i>struggle</i> with it until I knew I needed to stop eating so much of it. 
        I've honestly loved sugar as long as I can remember. From Culver's custard to Oreos and gummi peach rings, 
        I've tried a lot of different treats over the years. I long considered myself a "candy connoisseur".
    </p>
    <br />
    <p>
        Alas, my body blessed me with the resilience of my youth, only to betray me in my early 20s with 
        the formation of my first gut. I actually <i>lost</i> my "freshman 15" when I was in college, 
        only to <i>gain</i> my first 30 pounds of stress weight in my 4th year. I'm sure it had something to do with work, 
        almost not finishing my 4-year degree, and the relationship I was in at the time.
    </p>
    <br />
    <p>
        Then I gained some "happiness weight" after I met my now-wife in my 5th & final year of college. I believe I added another 30 
        by the time I started seeing a nutritionist a few years later to start addressing the root cause of my weight gain: <b>overconsuming sugar</b>. I tried a multitude of 
        strategies, diets, exercise regimens, and none of them seemed to stick. And when something failed to work, I returned to old habits. 
        With the way I was eating, my health was essentially in a downward spiral.
    </p>
    <br />
    <p>
        I became deeply unsatisfied with my inability to continue doing some of the parkour movements I used to enjoy. I'd let myself go to an extent that I couldn't 
        push or pull my body as I once could. As a result, 3 days before my peak weight, on May 12, 2022, I sustained my first major parkour 
        injury when I kong-vaulted over a metal picnic table and bashed my patellar tendon on the edge because my hips were not high enough 
        during the vault. All in all, I ended up ballooning from 200 lbs. in 2016 to 255 lbs. at my peak on May 15, 2022. 
    </p>
    <br />
    <p>
        I took this as a wake-up call that I needed to get my weight handled or I'd have no future with the activity I loved.  
        I wanted a way to get my life back, and to return to my parkour practice stronger than I'd ever been before. 
        So far, I've been able to shed about 20 pounds since last May, but I still have about 30 to go before I'm back 
        to that 200 lbs. mark.
    </p>
    <br />
    <p>
        I just turned 29 on April 2, and it made me realize that I had 1 more year to finish my list of "Before I'm 30" goals. On the 
        night of my birthday, I hatched a scheme that was so crazy it was kind of scary. And that's just the sort of challenge I'm excited 
        to try. I'd done the #100DaysOfCode challenge popular in the self-taught dev community. Twice. I loved that sort of timeboxed challenge 
        that seems doable but difficult. So I came up with this 365-day-long challenge where I won't eat any food with added sugar.
    </p>
    <br />
    <p>
        The strictness of the "no added sugar" rule is subject to my own judgment. I'm aiming for 0g per day, but there may be times where 
        the only food options available have added sugar in them. I figure, if the added sugar is small enough to not exceed the "25g per day" limit, 
        recommended by the FDA, I can use my judgment to determine if I want to consume that food. The way I see it, I'm going to be avoiding every major 
        offender in the book, from candy & cookies to ice cream & soda. If some manages to slip into my diet in small amounts, it likely won't be adding 
        up to a problematic quantity and I'll still see the health benefits I wanted out of this challenge in the first place. And most importantly, I won't 
        quit this challenge before the 365 days are up. I'll just keep treating every day as an opportunity to hit the 0g goal until I turn 30. And of course, 
        exceptional circumstances apply to anything (like, for example, an international vacation).
    </p>
    <br />
    <p>
        I'm going to continue to update this blog with entries about how it's going. My aim is to make 1 
